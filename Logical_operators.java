package com.example.helloworld;

public class Logical_operators {
    public static void main(String[] args) {

        int a = 5;
        int b = 3;

        boolean answer = a==5 || b==2;
        System.out.println(answer);

        boolean answer2 = a==5 && b==2;
        System.out.println(answer2);

    }
}
